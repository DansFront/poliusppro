          <article id="compromisso-com-a-preservacao-da-privacidade">
            <h2>Compromisso com a preservação da privacidade</h2>

            <p>A POLI USP PRO está comprometida com a preservação da sua privacidade. Portanto, o objetivo deste documento é esclarecer quais informações são coletadas dos usuários dos sites mantidos pela POLI USP PRO e respectivos serviços, assim como a forma com que esses dados são manipulados e utilizados.</p>

            <p>Caso não concorde com o conteúdo desta política, não é recomendável que você baixe os materiais e nem utilize quaisquer dos serviços disponibilizados pela POLI USP PRO</p>
          </article>

          <article id="como-sao-coletados-os-seus-dados">
            <h2>Como são coletados os seus dados</h2>
            <p>Os dados são coletados pelos sites mantidos pela POLI USP PRO e pelos canais de contato com a POLI USP PRO.</p>
          </article>

          <article id="formas-de-coleta-das-informacoes">
            <h2>Formas de coleta das informações</h2>
            <ol>
              <li><strong>Informações fornecidas por você –</strong> A POLI USP PRO coleta informações de identificação pessoal – como nome, gênero, estado civil, profissão, escolaridade, número de documentos pessoais, telefone, correio eletrônico, empresa em que trabalha, cargo etc. – via preenchimento de formulários.</li>

              <li><strong>Informações de navegação no site (cookies) –</strong> Quando você visita nossos sites, é inserido um “cookie” no seu navegador, por meio de softwares como o Google Analytics entre outros, para identificar quantas vezes você retornou ao nosso endereço. São coletadas, anonimamente, informações como endereço IP, localização geográfica, fonte de referência, tipo de navegador, duração da visita e páginas visitadas. Veja mais detalhes no tópico “Sobre os cookies”.</li>

              <li><strong>Histórico de contato –</strong> Se você enviar um correio eletrônico para a POLI USP PRO ou entrar em contato, fornecendo os seus dados por algum outro canal como o WhatsApp, por exemplo, serão armazenadas as informações fornecidas, bem como todo o histórico de contatos realizados. O objetivo é personalizar o atendimento e manter registros das interações que você tem com a POLI USP PRO.</li>

              <li><strong>Eventos –</strong> Se você se cadastrou em algum evento organizado ou apoiado pela POLI USP PRO, é possível que seus dados tenham sido coletados.</li>
            </ol>
          </article>

          <article id="sobre-os-cookies">
            <h2>Sobre os cookies</h2>

            <p>Cookies são arquivos armazenados pelo navegador enquanto você acessa a Internet. Eles podem ser utilizados para compartilhar dados sobre suas atividades e acessos em nossos sites, como idioma de acesso e dispositivo utilizado, por exemplo.</p>

            <p>A POLI USP PRO utiliza cookies para saber quantas vezes você retornou ao site, quais páginas você acessou e quanto tempo permaneceu entre outras informações.</p>
            
            <p>O objetivo da utilização dos cookies é armazenar dados sobre a experiência dos usuários no referido site e, a partir desses dados, melhorar a experiência de navegação.</p>
          </article>
          
          <article id="quais-dados-sao-coletados">
            <h2>Quais dados são coletados:</h2>

            <p>Por meio de formulários e eventos</p>

            <p>Dados de perfil: informações referentes ao perfil do usuário, como nome, foto de perfil, link para suas redes sociais.</p>

            <p>Informações de contato: informações de contato do usuário, como correio eletrônico, WhatsApp, endereço e telefone</p>

            <p>Informações da empresa: informações referentes à empresa em que o usuário trabalha, como nome, site, número de colaboradores, setor de atuação, entre outros.</p>

            <p>Informações de cobrança: número do CPF ou CNPJ, dados do cartão de crédito (não são armazenados, somente utilizados para o pagamento).</p>

            <p>Conteúdo consumido:</p>

            <p>Coletamos as informações sobre todos os conteúdos consumidos pelo usuário em nossos sites (como arquivos baixados e vídeos assistidos).</p>

            <p>Por meio de cookies:</p>

            <p>Preferências e páginas acessadas: são salvas informações referentes às preferências de conteúdo dos usuários. Exemplo: quais páginas foram acessadas e informações sobre o sistema operacional e os dispositivos que foram utilizados para acessar o site.</p>

            <p>Dados geográficos: podem ser armazenadas informações referentes à localização física do usuário.</p>

            <p>Dados demográficos: em alguns casos, podem ser armazenados dados demográficos, como idade, gênero, nível de escolaridade e renda aproximada dos usuários.</p>

            <p>Por meio de outros canais de contato (correio eletrônico, WhatsApp, chat e telefone):</p>

            <p>Histórico de conversas: ao contatar o suporte, departamento comercial, administrativo, financeiro ou marketing, poderemos armazenar o histórico da conversa (seja ela por correio eletrônico, WhatsApp, chat ou telefone).</p>
          </article>

          <article id="por-que-sao-capturados-os-seus-dados">
            <h2>Por que são coletados os seus dados</h2>
            <p>Todas as suas informações pessoais, recolhidas pelos meios supracitados, serão utilizadas para a promoção de melhorias, aperfeiçoando e enriquecendo a sua
            experiência de visita aos sites mantidos pela POLI USP PRO.</p>
          </article>

          <article id="quem-podera-acessar-os-seus-dados">
            <h2>Quem poderá acessar os seus dados</h2>

            <p>Apenas integrantes da equipe POLI USP PRO e funcionários da Universidade de São
            Paulo poderão ver as suas informações pessoais. Eventualmente, caso a inserção de
            suas informações se dê em ações criadas em parcerias, os parceiros, explicitamente
            identificados, também terão acesso às informações. Nenhuma informação pessoal
            poderá ser divulgada publicamente, em nenhuma circunstância. </p>

            <p>A POLI USP PRO também se compromete a não vender, alugar ou repassar suas
            informações para terceiros. A única exceção diz respeito às informações que,
            porventura, forem exigidas judicialmente ou pela Administração Pública brasileira</p>
          </article>

          <article id="como-seus-dados-sao-protegidos">
            <h2>Como seus dados são protegidos</h2>

            <p>Todas as informações coletadas, relativas a pessoas que acessarem os sites da POLI
            USP PRO, serão tratadas em concordância com a Lei Geral de Proteção de Dados –
            LGPD.</p>

            <p>Você poderá verificar, a qualquer momento, quais dos seus dados constam nos
            registros da POLI USP PRO. Para isso, basta enviar um correio eletrônico à central de
            contato.</p>

            <p>Caso prefira, você também poderá solicitar que os seus dados sejam apagados e/ou
            alterados, bastando, para tanto, que essa solicitação seja feita no mesmo canal de
            contato.</p>

            <p>A POLI USP PRO protege a confidencialidade e a segurança das suas informações por
            meio de softwares hospedados em servidores que atendem aos mais elevados níveis
            de segurança.</p>

            <p>Embora sejam adotadas excelentes práticas de proteção e segurança, nenhum serviço
            na rede mundial de computadores possui 100% de garantia contra invasões. Por isso, a
            POLI USP PRO não se responsabiliza por atos criminosos de terceiros, casos fortuitos
            ou de força maior.</p>
          </article>

          <article id="direito-dos-usuarios">
            <h2>Direito dos usuários</h2>

            <p>Qualquer formulário preenchido novamente poderá caracterizar a reinserção do seu
            correio eletrônico à lista. Caso seja de seu interesse, portanto, a requisição de
            cancelamento deverá ser feita novamente.</p>

            <p>Enquanto o usuário estiver inscrito em algum curso, não será possível cancelar o
            recebimento de correios eletrônicos referentes ao curso em andamento.</p>

            <p>Além disso, os usuários também têm:</p>

            <ul>
              <li>Direito de solicitar alteração dos dados cadastrados.</li>
              <li>Direito de saber quais dados estão sendo armazenados.</li>
              <li>Direito de receber explicação sobre como os seus dados são processados.</li>
            </ul>
          </article>

          <article id="por-quanto-tempo-os-dados-sao-armazenados">
            <h2>Por quanto tempo os dados são armazenados?</h2>

            <p>A POLI USP PRO armazenará os seus dados pelo tempo que julgar necessário para cumprir seus objetivos.</p>

            <p>Após o término de um curso, o usuário poderá solicitar remoção do armazenamento dos seus dados pelo correio eletrônico lgpd@poliusppro.com</p>
          </article>

